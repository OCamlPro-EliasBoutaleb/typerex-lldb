type event_site = {
  ob : string;
  fn : string;
  fl : string;
  fi : string;
  fe : string;
}

type subevent_site = {
  ev : event_site;
  cob : string;
  cfn : string;
  cfi : string;
}

type event_kind =
| Event of event_site
| SubEvent of subevent_site

type event = int * int array * event_kind
